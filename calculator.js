class Calculator {
    constructor() {
        
    }
    add(first, second) {
        return first + second;
    }
    substract(first, second) {
        return first - second;
    }
    multiply(first, second) {
        return first * second;
    }
    divide(first, second) {
        return first / second;
    }
}
    module.exports = Calculator;